from random import random

cases = 1000
cases_total = 1000
dead_probability = 0.01
cnt = 0
for i in range(cases * cases_total):
    rand = random()
    if rand <= dead_probability:
        cnt += 1

print("The estimated value is %.3f." % (cnt / cases))
