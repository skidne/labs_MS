from random import random

plants = 100
BlackMesaProbab = 0.001
cases = 10000

cnt = 0
for i in range(cases * plants):
    rand = random()
    if rand <= BlackMesaProbab:
        cnt += 1

print("The probability of at least one Black Mesa is %.3f" % (cnt / cases))
